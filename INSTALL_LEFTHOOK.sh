# Install lefthook globally (if you don't already have it)
# You can also use npx lefthook if you prefer not to install globally
brew install lefthook # macOS (with Homebrew)
# or
cargo install lefthook # Rust users
# or
curl -s https://raw.githubusercontent.com/evilmartians/lefthook/master/scripts/install.sh | bash

# Then, in your repo:
lefthook install